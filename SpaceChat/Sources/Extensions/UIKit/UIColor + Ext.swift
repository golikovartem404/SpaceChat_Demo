import Foundation
import UIKit

extension UIColor {

    static func buttonRed() -> UIColor {
        return #colorLiteral(red: 0.8924438357, green: 0, blue: 0, alpha: 1)
    }

    static func mainWhite() -> UIColor {
        return #colorLiteral(red: 0.968627451, green: 0.9725490196, blue: 0.9921568627, alpha: 1)
    }

    static func buttonBlack() -> UIColor {
        return #colorLiteral(red: 0.2, green: 0.2, blue: 0.2, alpha: 1)
    }

    static func textFieldBorder() -> UIColor {
        return #colorLiteral(red: 0.9019607843, green: 0.9019607843, blue: 0.9019607843, alpha: 1)
    }

    static func tabBarTintColor() -> UIColor {
        return #colorLiteral(red: 0.5568627451, green: 0.6470588235, blue: 0.968627451, alpha: 1)
    }

}
